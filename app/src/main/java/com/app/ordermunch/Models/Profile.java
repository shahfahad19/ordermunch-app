package com.app.ordermunch.Models;

public class Profile {
    public String name;
    public String email;
    public String contact;
    public String address;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getContact() {
        return contact;
    }

    public String getAddress() {
        return address;
    }
}
